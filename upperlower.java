public class upperlower 
{
public static void main(String args[])
{
	String str1 = "Swetha";
	System.out.println("The Original String is : " +str1);
	System.out.println("The String in Uppercase : " +str1.toUpperCase());
	System.out.println("The String in Lowercase : " +str1.toLowerCase());
}
}
