import java.util.*;
public class for_each_loop 
{
public static void main(String args[])
{
	ArrayList<String> a = new ArrayList<String>();  
	  a.add("Swetha");  
	  a.add("Sheela");  
	  a.add("Parthasarathy");  
	  a.add("Sooriya");  
	  for(String str:a)  
	  System.out.println(str);  

}
}
